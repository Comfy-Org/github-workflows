func TestEvaluate(t *testing.T) {
	t.Parallel()
	tests := []struct {
		name          string
		files         []FileChange
		max           int
		bypassed      bool
		wantCounted   int
		wantGenerated int
		wantOK        bool
	}{
		{
			name: "under cap passes",
			files: []FileChange{
				{Path: "a.go", Added: 100, Deleted: 50},
				{Path: "b.go", Added: 20, Deleted: 0},
			},
			max:         1000,
			wantCounted: 170,
			wantOK:      true,
		},
		{
			name: "over cap fails",
			files: []FileChange{
				{Path: "a.go", Added: 800, Deleted: 400},
			},
			max:         1000,
			wantCounted: 1200,
			wantOK:      false,
		},
		{
			name: "generated files excluded from count",
			files: []FileChange{
				{Path: "svc.gen.go", Added: 5000, Deleted: 2000, Generated: true},
				{Path: "hand.go", Added: 100, Deleted: 10},
			},
			max:           1000,
			wantCounted:   110,
			wantGenerated: 7000,
			wantOK:        true,
		},
		{
			name: "binary files contribute nothing",
			files: []FileChange{
				{Path: "logo.png", Binary: true},
				{Path: "hand.go", Added: 30, Deleted: 0},
			},
			max:         1000,
			wantCounted: 30,
			wantOK:      true,
		},
		{
			name: "bypass label passes even when over cap",
			files: []FileChange{
				{Path: "a.go", Added: 9000, Deleted: 0},
			},
			max:         1000,
			bypassed:    true,
			wantCounted: 9000,
			wantOK:      true,
		},
		{
			name: "exactly at cap passes",
			files: []FileChange{
				{Path: "a.go", Added: 1000, Deleted: 0},
			},
			max:         1000,
			wantCounted: 1000,
			wantOK:      true,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			t.Parallel()
			got := Evaluate(tt.files, tt.max, tt.bypassed)
			if got.Counted != tt.wantCounted {
				t.Errorf("Counted = %d, want %d", got.Counted, tt.wantCounted)
			}
			if got.Generated != tt.wantGenerated {
				t.Errorf("Generated = %d, want %d", got.Generated, tt.wantGenerated)
			}
			if got.OK != tt.wantOK {
				t.Errorf("OK = %v, want %v", got.OK, tt.wantOK)
			}
		})
	}
}

