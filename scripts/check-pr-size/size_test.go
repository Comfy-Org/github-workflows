// nul builds a NUL-terminated `git diff --numstat -z` stream from records. Each
// record is a preformatted "<added>\t<deleted>\t<path>" (or the two extra
// old/new path tokens of a rename); every token is terminated by a NUL.
func nul(records ...string) string {
	if len(records) == 0 {
		return ""
	}
	return strings.Join(records, "\x00") + "\x00"
}

func TestParseNumstat(t *testing.T) {
	t.Parallel()
	tests := []struct {
		name    string
		input   string
		want    []FileChange
		wantErr bool
	}{
		{
			name:  "simple add and delete",
			input: nul("10\t5\tpkg/foo/foo.go", "0\t3\tbar.go"),
			want: []FileChange{
				{Path: "pkg/foo/foo.go", Added: 10, Deleted: 5},
				{Path: "bar.go", Added: 0, Deleted: 3},
			},
		},
		{
			name:  "binary file",
			input: nul("-\t-\tassets/logo.png"),
			want:  []FileChange{{Path: "assets/logo.png", Binary: true}},
		},
		{
			name:  "stray empty tokens ignored",
			input: "\x00" + nul("2\t2\ta.go"),
			want:  []FileChange{{Path: "a.go", Added: 2, Deleted: 2}},
		},
		{
			// -z renames: empty path field, then old-path and new-path tokens.
			name:  "rename resolves to new path",
			input: nul("1\t1\t", "dir/old/file.go", "dir/new/file.go"),
			want:  []FileChange{{Path: "dir/new/file.go", Added: 1, Deleted: 1}},
		},
		{
			// A file whose literal name contains " => " must NOT be treated as a
			// rename (regression: the old newline parser rewrote it to "go.sum").
			name:  "literal arrow in filename is preserved",
			input: nul("1\t0\tnotes => go.sum"),
			want:  []FileChange{{Path: "notes => go.sum", Added: 1, Deleted: 0}},
		},
		{
			// Braces in a legit filename must not trigger rename rewriting.
			name:  "literal braces in filename are preserved",
			input: nul("3\t0\tconfig/{settings}.go"),
			want:  []FileChange{{Path: "config/{settings}.go", Added: 3, Deleted: 0}},
		},
		{
			// -z emits UTF-8/space paths verbatim (no C-style quoting).
			name:  "unicode path kept verbatim",
			input: nul("1\t0\tcafé/go.sum"),
			want:  []FileChange{{Path: "café/go.sum", Added: 1, Deleted: 0}},
		},
		{
			name:    "malformed record errors",
			input:   nul("not-a-numstat-record"),
			wantErr: true,
		},
		{
			name:    "non-numeric count errors",
			input:   nul("x\t2\tfoo.go"),
			wantErr: true,
		},
		{
			name:    "truncated rename errors",
			input:   nul("1\t1\t", "only-old-path"),
			wantErr: true,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			t.Parallel()
			got, err := ParseNumstat(strings.NewReader(tt.input))
			if tt.wantErr {
				if err == nil {
					t.Fatalf("expected error, got nil")
				}
				return
			}
			if err != nil {
				t.Fatalf("unexpected error: %v", err)
			}
			if len(got) != len(tt.want) {
				t.Fatalf("got %d files, want %d: %+v", len(got), len(tt.want), got)
			}
			for i := range got {
				if got[i] != tt.want[i] {
					t.Errorf("file %d = %+v, want %+v", i, got[i], tt.want[i])
				}
			}
		})
	}
}

